INSERT INTO DEPT_TBL(ID,DEPT_NAME) VALUES  
(1,'IT'), (2,'SALES'); 
 
SELECT * FROM EMP_TBL;
SELECT * FROM DEPT_TBL;
SELECT D.DEPT_NAME AS [DEPARTMENT NAME], 
E.NAME AS [EMPLOYEE NAME], E.SALARY  
FROM EMP_TBL AS E  
INNER JOIN DEPT_TBL AS D  
ON E.DEPT_ID = D.ID  
WHERE E.SALARY IN( 
SELECT MAX(SALARY)  
FROM EMP_TBL WHERE DEPT_ID=E.DEPT_ID)  
ORDER BY D.ID; 
 


