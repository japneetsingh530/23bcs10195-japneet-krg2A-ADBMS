CREATE TABLE TBL_A( 
EmpID INT PRIMARY KEY, 
Ename VARCHAR(100),
Salary INT 
); 
CREATE TABLE TBL_B( 
EmpID INT PRIMARY KEY, 
Ename VARCHAR(100), 
Salary INT 
); 
 
INSERT INTO TBL_A(EmpID,Ename,Salary) VALUES 
