(2,'BB',300); 
INSERT INTO TBL_B(EmpID,Ename,Salary) VALUES 
(2,'BB',400), 
(3,'CC',100); 
SELECT * FROM TBL_A;
SELECT * FROM TBL_B;
SELECT EmpID,MIN(Ename) AS Ename,MIN(Salary) AS [Salary] 
FROM (
    SELECT EmpID,Ename,Salary FROM TBL_A 
	UNION ALL 
	SELECT EmpID,Ename,Salary FROM TBL_B 
) AS MERGEDDATA 
GROUP BY EmpID;
