CREATE TABLE EMP_TBL( 
ID INT PRIMARY KEY, 
NAME VARCHAR(60), 
SALARY INT, 
DEPT_ID INT 
); 
CREATE TABLE DEPT_TBL( 
ID INT PRIMARY KEY, 
DEPT_NAME VARCHAR(100) 
); 
 

